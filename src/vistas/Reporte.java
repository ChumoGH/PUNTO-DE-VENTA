void listarReporte(){
    List<Ventas> lista=ccon.listarRepor();
    modelo=(DefaultTableModel)TablaReporte.getModel();
    Object[]ob=new Object[5];
    for (int i = 0; i < lista.size() ; i++) {
        ob[0]=lista.get(i).getIdv();
        ob[1]=lista.get(i).getNuse();
        ob[2]=lista.get(i).getIdC();
        ob[3]=lista.get(i).getFeV();
        ob[4]=lista.get(i).getMo();
        
        modelo.addRow(ob);
    }
     TablaReporte.setModel(modelo);
}
