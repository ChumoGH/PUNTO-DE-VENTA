     void agregarP(){
     String co=txtCodigo.getText();
     String np=txtNomP.getText();
     String pp=txtPrecio.getText();
     String st=txtStock.getText();
     
     Object[] ob=new Object[4];
     ob[0]=co;
     ob[1]=np;
     ob[2]=pp;
     ob[3]=st;
     
     ccon.addPeli(ob);
     }
     
     void actualizarP(){
     int fila=TablaPeli.getSelectedRow();
         if (fila==-1) {
             JOptionPane.showMessageDialog(this,"Por favor selecione una fila para continuear");
         }else{
     String co=txtCodigo.getText();
     String np=txtNomP.getText();
     String pp=txtPrecio.getText();
     String st=txtStock.getText();
    
     Object[] obp=new Object[5];
     obp[0]=co;
     obp[1]=np;
     obp[2]=pp;
     obp[3]=st;
     obp[4]=ip;
     ccon.actualizarPeli(obp);
        }
     }
     void eliminarP(){
     int fila=TablaPeli.getSelectedRow(); 
         if (fila==-1) {
             JOptionPane.showMessageDialog(this,"Por favor selecione una fila para continuear");
         }else{
           int ids=Integer.parseInt(TablaPeli.getValueAt(fila,0).toString());
           ccon.eliminarPeli(ids);
         }
     }
     
     void nuevoP(){
     
     txtCodigo.setText("");    
     txtNomP.setText("");
     txtPrecio.setText("");
     txtStock.setText(""); 
     txtCodigo.requestFocus();
     
     }
     
     void limpiarP(){
         for (int i = 0; i < modelo.getRowCount(); i++) {
             modelo.removeRow(i);
             i=i-1;
         }
     }
    
