void listarP(){
    List<EntPeliculas> lista=ccon.listarPeli();
    modelo=(DefaultTableModel)TablaPeli.getModel();
    Object[]ob=new Object[5];
    for (int i = 0; i < lista.size() ; i++) {
        ob[0]=lista.get(i).getIdp();
        ob[1]=lista.get(i).getCo();
        ob[2]=lista.get(i).getNop();
        ob[3]=lista.get(i).getPre();
        ob[4]=lista.get(i).getStok();
        
        modelo.addRow(ob);
    }
     TablaPeli.setModel(modelo);
}

