    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        // TODO add your handling code here:
        int fila=Tabla.getSelectedRow();
        if (fila==-1) {
            JOptionPane.showMessageDialog(this, "Debe Selecionar una Fila");
             
        }else{
     idd= Integer.parseInt(Tabla.getValueAt(fila,0).toString());
     String cd=Tabla.getValueAt(fila,1).toString();
     String nom=Tabla.getValueAt(fila,2).toString();
     String ape=Tabla.getValueAt(fila,3).toString();
     String dir=Tabla.getValueAt(fila,4).toString();
     String tef=Tabla.getValueAt(fila,5).toString();
     txtcd.setText(cd);
     txtnombre.setText(nom);
     txtApellido.setText(ape);
     txtdireccion.setText(dir);
     txtTelefono.setText(tef);
        }
    }//GEN-LAST:event_TablaMouseClicked

