     void agregar(){
     String cd=txtcd.getText();
     String nom=txtnombre.getText();
     String ape=txtApellido.getText();
     String dir=txtdireccion.getText();
     String tef=txtTelefono.getText();
     Object[] ob=new Object[5];
     ob[0]=cd;
     ob[1]=nom;
     ob[2]=ape;
     ob[3]=dir;
     ob[4]=tef;
     ccon.add(ob);
     }
     
     void actualizar(){
     int fila=Tabla.getSelectedRow();
         if (fila==-1) {
             JOptionPane.showMessageDialog(this,"Por favor selecione una fila para continuear");
         }else{
     String cd=txtcd.getText();
     String nom=txtnombre.getText();
     String ape=txtApellido.getText();
     String dir=txtdireccion.getText();
     String tef=txtTelefono.getText();
     Object[] obt=new Object[6];
     obt[0]=cd;
     obt[1]=nom;
     obt[2]=ape;
     obt[3]=dir;
     obt[4]=tef;
     obt[5]=idd;
     ccon.actualizar(obt);
        }
     }
     void eliminar(){
     int fila=Tabla.getSelectedRow(); 
         if (fila==-1) {
             JOptionPane.showMessageDialog(this,"Por favor selecione una fila para continuear");
         }else{
           int ids=Integer.parseInt(Tabla.getValueAt(fila,0).toString());
           ccon.eliminar(ids);
         }
     }
     
     void nuevo(){
     txtcd.setText("");
     txtnombre.setText("");
     txtApellido.setText("");
     txtdireccion.setText("");
     txtTelefono.setText("");
     txtcd.requestFocus();
     
     }
     
     void limpiar(){
         for (int i = 0; i < modelo.getRowCount(); i++) {
             modelo.removeRow(i);
             i=i-1;
         }
     }
     
